package bookshelf;

import java.awt.Color;

public class books {

	String name;
	int rating;
	int w, h;
	Color c;
	int x, y;
	
	public books(String name, int rating, int w, int h, Color c, int x, int y) {
		
		this.name = name;
		this.rating = rating;
		this.w = w;
		this.h = h;
		this.c = c;
		this.x = x;
		this.y = y;
		
	}
	
}
